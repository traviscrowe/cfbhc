class Player(object):
    def _init_(self, throw_power, throw_accuracy, speed, break_tackle, catching,
    agility, blocking, pass_protect, acceleration, pass_rush, run_defense,
    coverage, tackle, kick_power, kick_accuracy, concentration, position):
        self.throw_power = throw_power
        self.throw_accuracy = throw_accuracy
        self.speed = speed
        self.break_tackle = break_tackle
        self.catching = catching
        self.agility = agility
        self.blocking = blocking
        self.pass_protect = pass_protect
        self.acceleration = acceleration
        self.pass_rush = pass_rush
        self.run_defense = run_defense
        self.coverage = coverage
        self.tackle = tackle
        self.kick_power = kick_power
        self.kick_accuracy = kick_accuracy
        self.concentration = concentration
        self.position = position
